class Currency {
  String? info, description, timestamp;
  Rate? rate;

  Currency({this.info, this.description, this.timestamp, this.rate});

  factory Currency.fromJson(Map<String, dynamic> data) {
    Rate rate = Rate.fromJson(data["rates"]);
    return Currency(
        info: data["info"],
        description: data["description"],
        timestamp: data["timestamp"],
        rate: rate);
  }
}

